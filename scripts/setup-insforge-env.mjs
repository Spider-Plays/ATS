import { execSync } from 'node:child_process'
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const projectPath = path.join(root, '.insforge', 'project.json')

if (!existsSync(projectPath)) {
  console.error('Missing .insforge/project.json — run: npx @insforge/cli link')
  process.exit(1)
}

const project = JSON.parse(readFileSync(projectPath, 'utf8'))
const insforgeUrl = project.oss_host
const apiKey = project.api_key
const apiBaseUrl = 'https://stitch-ats-api-6dd11de4-0043-4523-80ec-1b8755a5d51c.fly.dev'
const siteUrl = 'https://stitch-ats.insforge.site'
const clientOrigins = [
  'http://localhost:3000',
  'https://stitch-ats.in',
  'https://www.stitch-ats.in',
  'https://stitch-ats.insforge.site',
  'https://3ixy53ge.insforge.site',
].join(',')

function run(cmd) {
  return execSync(cmd, { cwd: root, encoding: 'utf8', stdio: ['pipe', 'pipe', 'pipe'] }).trim()
}

let databaseUrl = ''
let anonKey = ''

try {
  databaseUrl = run('npx @insforge/cli db connection-string')
} catch (err) {
  console.error('Failed to fetch DATABASE_URL:', err.stderr?.toString() || err.message)
  process.exit(1)
}

function parseSecretValue(raw) {
  const line = raw.split(/\r?\n/).find((l) => l.trim())?.trim() ?? ''
  if (line.includes('=')) {
    return line.split('=').slice(1).join('=').trim().replace(/^["']|["']$/g, '')
  }
  return line.replace(/^["']|["']$/g, '')
}

try {
  anonKey = parseSecretValue(run('npx @insforge/cli secrets get ANON_KEY'))
} catch {
  console.warn('Could not fetch ANON_KEY automatically — set VITE_INSFORGE_ANON_KEY manually.')
}

const serverEnvPath = path.join(root, 'server', '.env')
const clientEnvPath = path.join(root, '.env')

const serverLines = [
  '# Generated by npm run insforge:setup',
  `DATABASE_URL="${databaseUrl}"`,
  `INSFORGE_URL="${insforgeUrl}"`,
  `INSFORGE_API_KEY="${apiKey}"`,
  `INSFORGE_ANON_KEY="${anonKey}"`,
  'JWT_SECRET="dev-insforge-jwt-secret-change-in-production-32chars"',
  'PORT=4000',
  'CLIENT_ORIGIN="' + clientOrigins + '"',
  'APP_NAME="Stitch ATS"',
  '',
]

const clientLines = [
  '# Generated by npm run insforge:setup',
  `VITE_INSFORGE_URL="${insforgeUrl}"`,
  `VITE_INSFORGE_ANON_KEY="${anonKey}"`,
  '# Local dev uses Vite proxy — see .env.development (do not set Fly API URL here).',
  'VITE_API_BASE_URL=',
  '',
]

writeFileSync(serverEnvPath, serverLines.join('\n'), 'utf8')
writeFileSync(clientEnvPath, clientLines.join('\n'), 'utf8')

console.log(`Wrote ${serverEnvPath}`)
console.log(`Wrote ${clientEnvPath}`)
console.log(`Production site: ${siteUrl}`)
console.log('Next: npm run insforge:config:apply && npm run db:seed --prefix server')
